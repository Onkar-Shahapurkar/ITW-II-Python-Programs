my_sample_set1 = {1, 2, 3, 4}
my_sample_set2 = {5, 6, 7, 4, 8}

# Union of my_sample_set1 and my_sample_set2
my_sample_set3 = my_sample_set1 | my_sample_set2
print("Union of my_sample_set1&my_sample_set2: my_sample_set3 = ", my_sample_set3)

# Intersection of my_sample_set1 and my_sample_set2
my_sample_set4 = my_sample_set1 & my_sample_set2
print("Intersection of my_sample_set1 & my_sample_set2: my_sample_set4 = ", my_sample_set4)
print("\n")