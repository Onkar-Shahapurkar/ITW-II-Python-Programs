# Delete a key using .pop()

sample_dictionary = {
    70 : "Janhavi",
    71 : "Akshita",
    72: "Dhiraj",
    73: "Ambika",
    74: "Ajay",
    75: "Mayuri",
    76: "Ayush",
    77: "Onkar"
}

